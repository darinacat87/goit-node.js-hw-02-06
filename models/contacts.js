const { Schema, model } = require("mongoose");
const Joi = require("joi");

const contactSchemaModel = Schema(
  {
    name: {
      type: String,
      required: [true, "Set name for contact"],
    },
    email: {
      type: String,
    },
    phone: {
      type: String,
    },
    favorite: {
      type: Boolean,
      default: false,
    },
  },
  { versionKey: false, timestamps: true }
);

const Contact = model("contact", contactSchemaModel);

module.exports = {
  Contact,
};
